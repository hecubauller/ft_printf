#include "ft_printf.h"

char    *ft_p(unsigned long long p)
{
    char    *s;
    char    *r;
    int     i;

    s = ft_itoa_base(p, 16, 1);
    r = (char *)malloc(sizeof(char) * (strlen(s) + 3));
    r[0] = '0';
    r[1] = 'x';
    i = -1;
    while (s[++i])
        r[i + 2] = s[i];
    r[i + 2] = '\0';
    free(s);
    return (r);
}