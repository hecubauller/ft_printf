#include "ft_printf.h"

char    *str_ad(char *s1, char *s2)
{
    int     i;
    i = ft_strlen(s1);
    while (--i > -1)
    {
        s1[i] = s1[i] - '0' + s2[i];
        if (s1[i] > '9')
        {
            s1[i] -= 10;
            ++s1[i - 1];
        }
    }
    return (s1);
}
