#include "ft_printf.h"

void    rnd_z(char *s, int i)
{
    char    *t;
    int     l;

    if (s[i + 1] < '5' || (s[i + 1] == '5' && !(s[i - 1] % 2)))
        s[i + 1] = '\0';
    else if (s[i + 1] > '5' ||
             (s[i + 1] == '5' && s[i - 1] % 2))
    {
        s[i] = '\0';
        l = strlen(s);
        t = (char *) malloc(sizeof(char) * (l + 1));
        memset(t, '0', l);
        t[l] = '\0';
        t[l - 1] = '1';
        s = str_ad(s, t);
        free(t);
    }
}