#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	int		i;

	p = (char *)s;
	i = 0;
	while (p[i])
		i++;
	while (i >= 0)
	{
		if (p[i] == c)
			return (p + i);
		i--;
	}
	return (NULL);
}
