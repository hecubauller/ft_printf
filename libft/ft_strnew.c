#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;

	if (size == 4294967295)
		return (NULL);
	return ((s = (char *)malloc(sizeof(char) * (size + 1))) ?
			ft_memset(s, 0, size + 1) : NULL);
}
