#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*r;
	int		i;

	i = 0;
	while (s1[i])
		i++;
	r = (char *)malloc(sizeof(char) * (i + 1));
	if (r)
	{
		i = 0;
		while (s1[i])
		{
			r[i] = s1[i];
			i++;
		}
		r[i] = '\0';
		return (r);
	}
	return (NULL);
}
